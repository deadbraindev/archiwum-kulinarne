export const isFavorite = (slug) => {
  try {
    const localFavorite = JSON.parse(localStorage.getItem('favorites'));
    if (localFavorite && localFavorite.some((e) => e.slug === slug)) {
      return true;
    }
    return false;
  } catch (error) {
    // console.log('error in isFavorite check, RecipeUtilities');
  }
  return null;
};

